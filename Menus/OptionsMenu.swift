    """
    
    print(menu)
}

func bagMenu(bag: Bag) {
    var items: [String] = []
    
    var i = 0
    for item in bag.getItems() {
        i += 1
        items.append(countSpaces(name: "\(i). \(item.key.getName()) x \(item.value)", size: 64))
    }
    let back = countSpaces(name: "4. Return", size: 64)
    
    let menu = """
                                                ═══════════════════════════════════════════════════════════════ஜ۩۞۩ஜ══════════════════════════════════════════════════════════════
