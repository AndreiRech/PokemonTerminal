func fightMenu(pokemon: Pokemon) {
    var attacks: [String] = []
    
    var i = 0
    for attack in pokemon.getMoves() {
        i += 1
        attacks.append(countSpaces(name: "\(i). \(attack.getName()) x \(attack.getPp())", size: 64))
    }
    
    let menu = """
                                                ═══════════════════════════════════════════════════════════════ஜ۩۞۩ஜ══════════════════════════════════════════════════════════════
