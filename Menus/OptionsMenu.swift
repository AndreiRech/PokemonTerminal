                                                |\(items[2])|\(back)|
                                                ══════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════
    """
    
    print(menu)
}

func pokemonMenu(trainer: Trainer) {
    var pokemons: [String] = []
    
    var i = 0
    for pokemon in trainer.getTeam() {
        i += 1
        if !pokemon.getIsAlive() {
            pokemons.append(countSpaces(name: "\(i). \(pokemon.getName()) ❌", size: 63))
        } else {
            pokemons.append(countSpaces(name: "\(i). \(pokemon.getName())", size: 64))
        }
    }
    let back = countSpaces(name: "4. Return", size: 64)
    
    let menu = """
                                                ═══════════════════════════════════════════════════════════════ஜ۩۞۩ஜ══════════════════════════════════════════════════════════════
                                                |\(pokemons[0])|\(pokemons[1])|
                                                |----------------------------------------------------------------|----------------------------------------------------------------|  
                                                |\(pokemons[2])|\(back)|
                                                ══════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════════
    """
    
    print(menu)
}

